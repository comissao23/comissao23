<script>
import { mapState, mapMutations } from 'vuex'

export default {
    data() {
        return {
            investTerms: false,
            privacyTermsModal: false,
            financialTermsModal: false
        }
    },
    methods: {
        ...mapMutations({
            setLoginModal: 'modal/setLoginModal'
        }),
        async showLoginModal(modalString) {
            const userRoles = this.$userService.getType()
            if (this.$axios.hasAuthToken() && userRoles) {
                this.$router.push(
                    await this.$userService.loginRedirectionRoute(userRoles)
                )
            } else {
                this.openModal(
                    this.setLoginModal({
                        dialog: true,
                        type: modalString
                    })
                )
            }
        },
        openModal(modalType) {
            this.setLoginModal({
                dialog: true,
                type: modalType
            })
        },
        changeLoginModal(newVal) {
            this.setLoginModal({
                dialog: newVal
            })
        },
        changeLoginType(newVal) {
            this.setLoginModal({
                dialog: this.loginModal,
                type: newVal
            })
        },
        openTerms(userType, termType) {
            const routeData = this.$router.resolve({
                name: 'termos-userType-termType',
                params: { userType: userType, termType: termType }
            })
            window.open(routeData.href, '_blank')
        }
    },
    computed: {
        ...mapState({
            loginModal: state => state.modal.loginModal.dialog,
            loginModalType: state => state.modal.loginModal.type
        })
    }
}
</script>

